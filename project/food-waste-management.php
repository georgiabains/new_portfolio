        <p class="description">
            Our solution was to design a mobile app which tracks the contents of an individual's fridge and pantry, and notifies the user when a food item is nearing its expiration date. In addition, we designed a Recipes page to provide inspiration for certain food items and a Donations map to help a user locate local food banks. I was responsible for designing the Recipes page.
        </p>

        <figure>
            <img src="../img/projects/food_waste_management/food_waste_management_storyboard.png">
            <figcaption>Two storyboards of our app which demonstrate the basic goals of our app, and its intended positive effect on the user. Left: me (Georgia Bains). Right: Isabella Kuo.</figcaption>
        </figure>

        <p class="description">
            While our high-fidelity prototype was constructed in Figma, the scope of the project involved conducting User Research questionnaires and interviews on our target audience (young adults) as well as industry professionals (food bank and grocery store employees). We used a combination of online social media (Reddit, Instagram) and networking (friends-of-friends) to collect our data.
            <br><br>
            We used our research and a number of design principles learned throughout the class, such as Jakob Nielen's Heuristics, to brainstorm, storyboard and paper prototype our final design.
        </p>

