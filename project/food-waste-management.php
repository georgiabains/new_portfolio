        <p class="description">
            I created this mobile app design as part of a group project in University of Toronto's CSC318: The Design of Interactive Computational Media course. Our team consisted of myself, Isabella Kuo, Alex Waddell, Faiyaz Huq, and Mohammad Ettouhami and our group, Psyched, was one of six teams (out of the class total of 20), which the instructor and TAs favoured, based on a critieria which included creativity, protoype design, and the quality of written reports.
            <br><br>
            The project's assignment was to create a digital solution to a non-digital problem that people faced in their day-to-day activities. We were tasked with brainstorming various problems and deciding, as a team, which problem we'd like to explore. We ultimately decided on tackling the issue of food waste among young adults, using a mobile app to facilitate food management.
        </p>

